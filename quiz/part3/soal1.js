let i = 0; // Set i = 0

console.log(`Looping maju dimana var i = ${i}`);
while (i < 10) {
    i++; // Add i by one every time
    console.log(`Nilai var i = ${i}`);
}

console.log(`Looping mundur dimana var i = ${i}`);

while (i > 0) {
    i--; // Reduce i by one every time
    console.log(`Nilai var i = ${i}`);
}